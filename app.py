def get_base64_image(image_path: str) -> str:
    """Read an image file and return its Base64-encoded string.

    Used to embed the hero background image directly in the CSS ``<style>``
    block so no additional static file serving is required by Streamlit.

    Args:
        image_path: Absolute or relative path to the image file.

    Returns:
        A Base64-encoded UTF-8 string suitable for use in a CSS
        ``url("data:image/jpeg;base64,...")`` declaration.

    Raises:
        FileNotFoundError: If *image_path* does not exist.
    """
    with open(image_path, "rb") as f:
        return base64.b64encode(f.read()).decode()


