"""
app.py
======
Streamlit web interface for the Virtual Pro FAQ Chatbot.

This module is the application entry point. It wires together the Streamlit UI
with the RAG engine (``rag_engine.py``) and the OpenAI Chat API to deliver an
interactive FAQ assistant.

Usage
-----
    streamlit run app.py

Environment
-----------
``OPENAI_API_KEY``
    Your OpenAI secret key. Can be set in ``.env`` or entered at runtime via
    the sidebar. Required when ``DEMO_MODE = False``.

Modes
-----
DEMO_MODE = True  (default)
    No API key needed. Queries are answered by a keyword-overlap search
    against ``faqs.json``. Responses are instant (no streaming).

DEMO_MODE = False
    Full RAG pipeline: the FAQ file is embedded with ``text-embedding-3-small``,
    stored in a FAISS index, and answers are generated by ``gpt-4o-mini``
    with streaming output.

Session State Keys
------------------
messages : list[dict]
    Chat history. Each entry has keys ``"role"`` (``"user"`` | ``"assistant"``),
    ``"content"`` (str), and optionally ``"sources"`` (list[str]) for assistant
    messages.
index : faiss.Index | None
    The loaded FAISS vector index. ``None`` until an index is built or loaded.
chunks : list[str] | None
    The list of raw FAQ text chunks parallel to the index vectors. ``None``
    until populated.
"""

