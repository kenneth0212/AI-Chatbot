# ── Helper functions ───────────────────────────────────────────────────────────

def try_load_index() -> bool:
    """Silently attempt to load a pre-built index from disk into session state.

    Called once on the first page render so a previously built index is
    immediately available without the user clicking "Build Index" again.

    Side effects:
        Sets ``st.session_state.index`` and ``st.session_state.chunks`` on
        success.

    Returns:
        ``True`` if the index was loaded successfully, ``False`` if the index
        files do not exist yet (i.e. :func:`rag_engine.build_index` has never
        been run in the current working directory).
    """
    try:
        idx, chunks = rag_engine.load_index()
        st.session_state.index = idx
        st.session_state.chunks = chunks
        return True
    except FileNotFoundError:
        return False


def index_ready() -> bool:
    """Return ``True`` if both the FAISS index and chunk list are loaded.

    A convenience guard used before calling :func:`answer_question` in
    production mode to avoid ``AttributeError`` on ``None``.

    Returns:
        ``True`` when ``st.session_state.index`` and
        ``st.session_state.chunks`` are both non-``None``.
    """
    return st.session_state.index is not None and st.session_state.chunks is not None


def answer_question(question: str) -> tuple[str, list[str]]:
    """Generate an answer for *question* and stream it into the Streamlit UI.

    Behaviour differs by mode:

    Demo mode (``DEMO_MODE = True``)
        Delegates to :func:`rag_engine.search_faqs_json`. The best-matching
        FAQ entry is displayed instantly (no streaming). Returns the matched
        answer and a one-element source list containing the matched question.

    Production mode (``DEMO_MODE = False``)
        1. Retrieves the top-``TOP_K`` FAQ chunks via FAISS nearest-neighbour
           search (:func:`rag_engine.retrieve`).
        2. Builds a system prompt that instructs ``gpt-4o-mini`` to answer
           **only** from the retrieved context.
        3. Sends the last 4 conversation turns (8 messages) as chat history
           so the model has conversational context without consuming excessive
           tokens.
        4. Streams the response token-by-token into a ``st.empty()``
           placeholder, appending ``"▌"`` as a live typing cursor.

    Args:
        question: The user's raw question string from the chat input.

    Returns:
        A ``(answer, sources)`` tuple where *answer* is the full response
        text and *sources* is a list of the FAQ chunk strings that were used
        as context (or the matched question in demo mode).

    Raises:
        ValueError:        If ``OPENAI_API_KEY`` is not set (production mode).
        openai.OpenAIError: On any API failure.
    """
    if DEMO_MODE:
        result = rag_engine.search_faqs_json(question)
        if result:
            matched_q, answer = result
            source = f"**Q:** {matched_q}"
        else:
            answer = "Sorry, I couldn't find a matching answer in the FAQ."
            source = "(No match found)"
        # Render immediately (no streaming in demo mode)
        placeholder = st.empty()
        placeholder.markdown(answer)
        return answer, [source]

    # ── Production mode ────────────────────────────────────────────────────
    client = rag_engine.get_client()

    # Step 1: retrieve the top-K most relevant FAQ chunks for this query
    context_chunks = rag_engine.retrieve(
        question, st.session_state.index, st.session_state.chunks
    )
    # Join chunks with a visible separator so the model can distinguish them
    context_text = "\n\n---\n\n".join(context_chunks)

    # Step 2: build the grounding system prompt
    system_prompt = (
        "You are a helpful FAQ assistant. Answer the user's question using ONLY "
        "the context provided below. If the answer is not in the context, say so "
        "honestly — do not make up information.\n\n"
        f"CONTEXT:\n{context_text}"
    )

    # Step 3: include the last 4 turns (= 8 messages) of conversation history
    history = [
        {"role": m["role"], "content": m["content"]}
        for m in st.session_state.messages[-8:]
    ]

    # Step 4: call the Chat API with streaming enabled
    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {"role": "system", "content": system_prompt},
            *history,
            {"role": "user", "content": question},
        ],
        temperature=0.2,   # Low temperature keeps answers factual and consistent
        max_tokens=150,    # FAQ answers are typically short
        stream=True,
    )

    # Step 5: accumulate tokens and update the placeholder on each delta
    full_response = ""
    placeholder = st.empty()
    for chunk in response:
        delta = chunk.choices[0].delta.content or ""
        full_response += delta
        placeholder.markdown(full_response + "▌")  # ▌ acts as a live cursor

    # Final render without the cursor
    placeholder.markdown(full_response)

    return full_response, context_chunks


